# Raridade da Arma
def raridade_arma(nome):
    if "lâmina maçante" in nome:
        return "⭐"
    elif "notas do aprendiz" in nome:
        return "⭐"
    elif "protetor de iniciantes" in nome:
        return "⭐"
    elif "espada larga waster" in nome:
        return "⭐"
    elif "arco de caça" in nome:
        return "⭐"
    

