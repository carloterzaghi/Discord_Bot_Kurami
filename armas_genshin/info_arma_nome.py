# Nome da Arma
def nome_arma(nome):
    if "lâmina maçante" in nome:
        return "Lâmina Maçante"
    elif "Vazio" in nome:
        return "Vazio"
    elif "notas do aprendiz" in nome:
        return "Notas do Aprendiz"
    elif "protetor de iniciantes" in nome:
        return "Protetor de Iniciantes"
    elif "espada larga waster" in nome:
        return "Espada Larga Waster"
    elif "arco de caça" in nome:
        return "Arco de Caça"